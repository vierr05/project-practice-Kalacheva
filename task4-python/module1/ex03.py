# Задание 3: Расчёт итоговой стоимости

# Запрос цены и количества
price = float(input("Введите цену единицы товара (руб.): "))
quantity = int(input("Введите количество единиц: "))

# Вычисление итоговой стоимости
total_cost = price * quantity

# Вывод результата с денежным обозначением
print(f"Итоговая стоимость: {total_cost} руб.")
