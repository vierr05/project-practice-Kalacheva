# Задание 4: Доход по банковскому вкладу

# Запрос суммы вклада и процентной ставки
deposit = float(input("Введите сумму вклада (руб.): "))
rate = float(input("Введите процентную ставку (% годовых): "))

# Вычисление дохода за год по формуле простого процента
income = deposit * rate / 100
total_amount = deposit + income

# Вывод результатов
print(f"Доход за год: {income} руб.")
print(f"Итоговая сумма вклада: {total_amount} руб.")
