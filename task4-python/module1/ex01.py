# Упражнение 1: Переменные и типы данных

# Создай переменные
name = "Виктория"
age = 20
height = 1.75
is_student = True

# Выведи информацию
print("=== Информация о студенте ===")
print(f"Имя: {name}")
print(f"Возраст: {age} лет")
print(f"Рост: {height} м")
print(f"Студент: {is_student}")

# Проверка типов
print("\n=== Типы данных ===")
print(f"type(name) = {type(name)}")
print(f"type(age) = {type(age)}")
print(f"type(height) = {type(height)}")
print(f"type(is_student) = {type(is_student)}")