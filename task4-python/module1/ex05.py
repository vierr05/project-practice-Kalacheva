# Задание 5: Конвертация валюты

# Запрос курса доллара и суммы в рублях
usd_rate = float(input("Введите курс доллара к рублю: "))
rub_amount = float(input("Введите сумму в рублях: "))

# Вычисление эквивалентной суммы в долларах
usd_amount = rub_amount / usd_rate

# Вывод результата с округлением до 2 знаков
print(f"Эквивалентная сумма в долларах: {round(usd_amount, 2)} USD")