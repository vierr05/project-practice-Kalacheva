# Задание 2: Классификация субъекта по выручке

# Запрос годовой выручки предприятия
revenue = float(input("Введите годовую выручку предприятия (руб.): "))

# Определение категории бизнеса
if revenue <= 1_000_000:
    category = "Микробизнес"
elif revenue <= 10_000_000:
    category = "Малый бизнес"
elif revenue <= 100_000_000:
    category = "Средний бизнес"
else:
    category = "Крупный бизнес"

# Вывод результата
print(f"Категория предприятия: {category}")