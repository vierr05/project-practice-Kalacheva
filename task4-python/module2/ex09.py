# Задание 4: Таблица доходности по ставке

# Запрос процентной ставки
rate = float(input("Введите процентную ставку (% годовых): "))

# Исходный капитал
capital = 100_000

# Вывод заголовка таблицы
print(f"\nТаблица доходности при ставке {rate}% годовых:")
print("-" * 40)

# Цикл for для вывода таблицы от 1 до 12 месяцев
for month in range(1, 13):
    # Вычисление суммы начисленных процентов
    interest = capital * (rate / 100) * (month / 12)
    print(f"Месяц {month:2d}: {interest:10.2f} руб.")