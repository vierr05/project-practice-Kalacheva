# Задание 3: Расчёт налога на доходы физических лиц

# Запрос ежемесячной заработной платы
salary = float(input("Введите ежемесячную заработную плату (руб.): "))

# Применение ставки НДФЛ
if salary <= 50_000:
    tax_rate = 0.13
else:
    tax_rate = 0.15

# Вычисление суммы налога и зарплаты "на руки"
tax_amount = salary * tax_rate
net_salary = salary - tax_amount

# Вывод результатов
print(f"Сумма налога НДФЛ: {tax_amount:.2f} руб.")
print(f"Заработная плата на руки: {net_salary:.2f} руб.")