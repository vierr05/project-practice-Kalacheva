# Задание 1: Финансовый результат периода

# Запрос итоговой прибыли за отчётный месяц
profit = float(input("Введите итоговую прибыль за отчётный месяц (руб.): "))

# Условная конструкция для определения финансового результата
if profit > 0:
    print("Прибыль")
elif profit < 0:
    print("Убыток")
else:
    print("Безубыточность")